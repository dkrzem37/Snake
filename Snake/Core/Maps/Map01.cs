    public Map01(){
        wallRectanglesList = CreateCollisionMap(map);
        emptyBlocksList = CreateEmptyBlocksMap(map);
    }

    private List<Rectangle> CreateCollisionMap(int[,] map)
    {
        List<Rectangle> collMap = new List<Rectangle>(); 
        for(int i = 0; i< map.GetLength(0); ++i){
            for(int j = 0; j< map.GetLength(1); ++j){
                if(map[i,j] == 1)
                    collMap.Add(new Rectangle(j*30, i*30, Game1.tileSize, Game1.tileSize));
            }
        }
        return collMap;
    }

    private List<Rectangle> CreateEmptyBlocksMap(int[,] map)
    {
        List<Rectangle> emptyBlocksMap = new List<Rectangle>(); 
        for(int i = 0; i< map.GetLength(0); ++i){
            for(int j = 0; j< map.GetLength(1); ++j){
                if(map[i,j] == 0)
                    emptyBlocksMap.Add(new Rectangle(j*30, i*30, Game1.tileSize, Game1.tileSize));
            }
        }
        return emptyBlocksMap;
    }

